
            #######숫자형#######
      ## python은 들여쓰기를 잘해야한다. ##
                ## 정수형 ##
a = 1
print(a)

a = -123
print(a)

a = 0
print(a)

                ## 실수형 ##
                
a = 1.2
print(a)
a = -3.45
print(a)
a = 4.24E10
print(a)
a = 4.24e-10
print(a)


            ## 8진수와 16진수 ##
                  # 8진수
                
a = 0o177
print(a)

                  # 16진수
                  
a = 0x8ff
b = 0xABC
print(b)


                ## 사칙 연산 ##
a = 3
b = 4
c = a + b
print(c)

c = a - b
print(c)

c = a * b
print(c)

# 나누기
c = a / b
print(c)

# 제곱
c = a ** b
print(c)

# 나머지 연산
c = a % b
print(c)

# 나눗셈 후 몫을 리턴하는  // 연산자
c = a // b
print(c)

a = 1
a = a + 1
a += 1
print(a)

# 복합연산자 종류 
# [+=, -=, *=, /=, //=, %=, **=]

